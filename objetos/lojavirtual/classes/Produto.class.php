<?php

class Produto {
    
    private $televisao;
    private $monitor;
    private $celulares;

    public function setTelevisao() {
        return $this->televisao = $televisao;
    }

    public function setMonitor() {
        return $this->monitor = $monitor;
    }

    public function setCelulares() {
        return $this->celulares = $celulares;
    }


}